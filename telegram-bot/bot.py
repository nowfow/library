# –ò–º–ø–æ—Ä—Ç—ã –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏
from config.settings import settings
from config.database import db_manager

# –ò–º–ø–æ—Ä—Ç—ã middleware
from middleware.auth import auth_middleware
